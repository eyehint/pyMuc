def han_parse(name, line):
    n = 0
    for cmp in S_ATT:
        i = line.find(cmp)
        if i is not -1:
            s = cmp.split('/')
            if _is_jong(name):
                r = s[0][1:]
            else:
                r = s[1][0:-1]
            newline = line.replace(cmp, r)
            return name + newline
        n += 1
    return name + line

