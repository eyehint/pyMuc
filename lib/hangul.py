def is_han(word):
    if len(word) == 0: return False
    if len(word) % 2 == 1: return False
    
    for i in range(len(word)/2): 
        i = i * 2
        a = ord(word[i])
        b = ord(word[i+1])
    
        if  b >= 0xA1 and b <= 0xFE:
            if a < 0xB0 or a > 0xC8:
                return False
        else:
            return False
    return True

def _is_jong(word):
    a = ord(word[-2])
    b = ord(word[-1])

    if  b >= 0xA1 and b <= 0xFE:
        if a < 0xB0 or a > 0xC8:
            return 0
        i = ((a - 0xB0 ) * 94 + (b - 0xA1)) * 2;
        if _JONGSUNG[_TGCODE[i + 1] & 0x1F]:
            return 1
    return 0

